package it.polimi.ingsw.model.resources;

/**
 * Class which represents an amount of shields
 */
public class Shield extends Resource{

    /**
     * Default method
     */
    public Shield(){
        super();
    }

    /**
     * Parameterized constructor
     * @param numShields
     */
    public Shield(int numShields){
        super(numShields);
    }

}
